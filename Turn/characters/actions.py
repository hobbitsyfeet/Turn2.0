class action():
    def __init__(self):
        pass

    def attack(self):
        """
        Attacks a specific tile within range.
        """
        pass
    
    def defend(self):
        """
        Braces self, improving stats.
        """
        pass

    def sneak(self):
        """
        Toggle sneak, improving SNEAK stat and slowing movement.
        """
        pass

    def run(self):
        """
        Increases movement, consumes more stamina.
        """
        pass

    def interact(self):
        """
        Interacts with tile if close enough.
        """
        pass

    def pickpocket(self):
        """
        Checks the pocket of a character in a tile.
        """
        pass

    def talk(self):
        """
        Talks to a target tile.
        """
        pass

    def open_inventory(self):
        """
        Opens charactor's own inventory.
        """
        pass
    
    def close_inventory(self):
        """
        Closes charactor's own inventory.
        """
        pass